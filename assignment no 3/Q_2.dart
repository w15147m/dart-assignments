// Q.2: Write a program that prints the Fibonacci sequence up to a given
// number using a for loop.
void main() {
  int limit = 10;
  int first = 0;
  int second = 1;
  int next;

 for (var i = 0; first <= limit; i++) {

    next = first + second;
      first = second;
      second = next;
   print("Fibonacci sequence $next");
 }


}

   
