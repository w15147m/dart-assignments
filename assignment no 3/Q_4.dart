// Q.4: Implement a code that finds the factorial of a number using a while
// loop or for loop.
void main() {
    int num = 6;
    int factorial = 1;
    for (var i = 1; i <= num; i++) {
         factorial = factorial * i ; 
    }
    print("factorial : $factorial");
}

// out put
          // factorial : 720
     

