void main() {
// Q.8: Write a program to display a pattern like a right angle triangle using an
// asterisk using loop.

  for (var i = 1; i <= 5; i++) {
    for (var j = 1; j <= i; j++) {
      print('*');
    }
    print(''); 
  }
  
}

